package ezbake.data.common.graph;

public final class Coordinate {
    private final double latitude;
    private final double longitude;

    public Coordinate() {
        this.latitude = 0.0d;
        this.longitude = 0.0d;
    }

    public Coordinate(double latitude, double longitude) {
        this.latitude = latitude;
        this.longitude = longitude;
    }

    public double getLatitude() {
        return latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public ezbake.base.thrift.Coordinate convertToThriftCoordinate() {
        return new ezbake.base.thrift.Coordinate(latitude, longitude);
    }

    @Override
    public int hashCode() {
        int result;
        long temp;
        temp = Double.doubleToLongBits(latitude);
        result = (int) (temp ^ (temp >>> 32));
        temp = Double.doubleToLongBits(longitude);
        result = 31 * result + (int) (temp ^ (temp >>> 32));
        return result;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }

        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        final Coordinate other = (Coordinate) o;

        if (Double.compare(other.latitude, latitude) != 0) {
            return false;
        }

        return Double.compare(other.longitude, longitude) == 0;
    }
}
